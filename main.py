# DreamHack

quiz = [0x67, 0x55, 0x5c, 0x53, 0x5f, 0x5d, 0x55, 0x10]
xor_value = 0x30

result = [chr(element ^ xor_value) for element in quiz]

for element in result:
    print(element, end='')

